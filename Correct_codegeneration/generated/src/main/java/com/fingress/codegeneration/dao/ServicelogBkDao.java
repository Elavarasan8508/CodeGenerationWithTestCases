package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.ServicelogBk;
import com.bsit.codegeneration.mapper.ServicelogBkMapper;
import java.util.*;

@RegisterRowMapper(ServicelogBkMapper.class)
public interface ServicelogBkDao {

    @SqlQuery("SELECT * FROM SERVICELOG_BK")
    public List<ServicelogBk> findAll();

    @SqlQuery("SELECT * FROM SERVICELOG_BK WHERE id = :id")
    public Optional<ServicelogBk> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO SERVICELOG_BK(ID, SERVICE, OPERATION, ZONE, BRANCH, SOURCESYSTEM, TARGETSYSTEM, MASTERREFERENCE, EVENTREFERENCE, STATUS, PROCESSTIME, TIREQUEST, TIRESPONSE, BANKREQUEST, BANKRESPONSE, TIREQTIME, BANKREQTIME, BANKRESTIME, TIRESTIME, TRANSACTIONKEY1, STATICKEY1, NARRATIVE1, NARRATIVE2, ISRESUBMITTED, RESUBMITTEDCOUNT, RESUBMITTEDTIME, DESCRIPTION, TYPEFLAG, NODE, VALUEDATE, NUMBER1) VALUES (:ID, :SERVICE, :OPERATION, :ZONE, :BRANCH, :SOURCESYSTEM, :TARGETSYSTEM, :MASTERREFERENCE, :EVENTREFERENCE, :STATUS, :PROCESSTIME, :TIREQUEST, :TIRESPONSE, :BANKREQUEST, :BANKRESPONSE, :TIREQTIME, :BANKREQTIME, :BANKRESTIME, :TIRESTIME, :TRANSACTIONKEY1, :STATICKEY1, :NARRATIVE1, :NARRATIVE2, :ISRESUBMITTED, :RESUBMITTEDCOUNT, :RESUBMITTEDTIME, :DESCRIPTION, :TYPEFLAG, :NODE, :VALUEDATE, :NUMBER1)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() ServicelogBk entity);

    @SqlQuery("UPDATE SERVICELOG_BK SET ID = :ID, SERVICE = :SERVICE, OPERATION = :OPERATION, ZONE = :ZONE, BRANCH = :BRANCH, SOURCESYSTEM = :SOURCESYSTEM, TARGETSYSTEM = :TARGETSYSTEM, MASTERREFERENCE = :MASTERREFERENCE, EVENTREFERENCE = :EVENTREFERENCE, STATUS = :STATUS, PROCESSTIME = :PROCESSTIME, TIREQUEST = :TIREQUEST, TIRESPONSE = :TIRESPONSE, BANKREQUEST = :BANKREQUEST, BANKRESPONSE = :BANKRESPONSE, TIREQTIME = :TIREQTIME, BANKREQTIME = :BANKREQTIME, BANKRESTIME = :BANKRESTIME, TIRESTIME = :TIRESTIME, TRANSACTIONKEY1 = :TRANSACTIONKEY1, STATICKEY1 = :STATICKEY1, NARRATIVE1 = :NARRATIVE1, NARRATIVE2 = :NARRATIVE2, ISRESUBMITTED = :ISRESUBMITTED, RESUBMITTEDCOUNT = :RESUBMITTEDCOUNT, RESUBMITTEDTIME = :RESUBMITTEDTIME, DESCRIPTION = :DESCRIPTION, TYPEFLAG = :TYPEFLAG, NODE = :NODE, VALUEDATE = :VALUEDATE, NUMBER1 = :NUMBER1 WHERE id = :id")
    public int update(@BindBean() ServicelogBk entity);

    @SqlQuery("DELETE FROM SERVICELOG_BK WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
