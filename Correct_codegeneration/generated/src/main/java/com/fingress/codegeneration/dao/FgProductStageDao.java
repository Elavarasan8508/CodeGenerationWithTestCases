package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgProductStage;
import com.bsit.codegeneration.mapper.FgProductStageMapper;
import java.util.*;

@RegisterRowMapper(FgProductStageMapper.class)
public interface FgProductStageDao {

    @SqlQuery("SELECT * FROM FG_PRODUCT_STAGE")
    public List<FgProductStage> findAll();

    @SqlQuery("SELECT * FROM FG_PRODUCT_STAGE WHERE id = :id")
    public Optional<FgProductStage> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_PRODUCT_STAGE(ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE, STAGE_CODE, PERF_PTY_SUBTYP_CODE, PERF_PTY_TYP_CODE, PERF_PTY_ROLE_CODE, STP, STAGE_ORDER_IDX, AUTO_SUBMIT, ACTIVE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, ENTITY_REF_ID, AUTH_STATUS_CODE, TNX_STATUS_CODE, AUTHORIZATION, ENTITY_VERSION_ID, AUTO_AUTHORISE, PERF_PTY_FIELD_CODE, UPLOAD_REF_ID, ID) VALUES (:ENTITY_TYPE_CODE, :ENTITY_SUB_TYPE_CODE, :STAGE_CODE, :PERF_PTY_SUBTYP_CODE, :PERF_PTY_TYP_CODE, :PERF_PTY_ROLE_CODE, :STP, :STAGE_ORDER_IDX, :AUTO_SUBMIT, :ACTIVE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :ENTITY_REF_ID, :AUTH_STATUS_CODE, :TNX_STATUS_CODE, :AUTHORIZATION, :ENTITY_VERSION_ID, :AUTO_AUTHORISE, :PERF_PTY_FIELD_CODE, :UPLOAD_REF_ID, :ID)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgProductStage entity);

    @SqlQuery("UPDATE FG_PRODUCT_STAGE SET ENTITY_TYPE_CODE = :ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE = :ENTITY_SUB_TYPE_CODE, STAGE_CODE = :STAGE_CODE, PERF_PTY_SUBTYP_CODE = :PERF_PTY_SUBTYP_CODE, PERF_PTY_TYP_CODE = :PERF_PTY_TYP_CODE, PERF_PTY_ROLE_CODE = :PERF_PTY_ROLE_CODE, STP = :STP, STAGE_ORDER_IDX = :STAGE_ORDER_IDX, AUTO_SUBMIT = :AUTO_SUBMIT, ACTIVE_CODE = :ACTIVE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, ENTITY_REF_ID = :ENTITY_REF_ID, AUTH_STATUS_CODE = :AUTH_STATUS_CODE, TNX_STATUS_CODE = :TNX_STATUS_CODE, AUTHORIZATION = :AUTHORIZATION, ENTITY_VERSION_ID = :ENTITY_VERSION_ID, AUTO_AUTHORISE = :AUTO_AUTHORISE, PERF_PTY_FIELD_CODE = :PERF_PTY_FIELD_CODE, UPLOAD_REF_ID = :UPLOAD_REF_ID, ID = :ID WHERE id = :id")
    public int update(@BindBean() FgProductStage entity);

    @SqlQuery("DELETE FROM FG_PRODUCT_STAGE WHERE id = :id")
    public int delete(@Bind("id") String id);
}
