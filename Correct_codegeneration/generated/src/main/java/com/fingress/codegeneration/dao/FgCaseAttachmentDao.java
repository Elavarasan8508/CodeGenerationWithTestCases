package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgCaseAttachment;
import com.bsit.codegeneration.mapper.FgCaseAttachmentMapper;
import java.util.*;

@RegisterRowMapper(FgCaseAttachmentMapper.class)
public interface FgCaseAttachmentDao {

    @SqlQuery("SELECT * FROM FG_CASE_ATTACHMENT")
    public List<FgCaseAttachment> findAll();

    @SqlQuery("SELECT * FROM FG_CASE_ATTACHMENT WHERE id = :id")
    public Optional<FgCaseAttachment> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO FG_CASE_ATTACHMENT(ID, REFERENCE_ID, APPLICANT_PARTY_CODE, APPLICANT_REF_ID, ISSUING_PARTY_CODE, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, VERSION_ID, IS_MASTER_VERSION, PROCESS_ID, UPLOAD_REF_ID, FG_FILE_TITLE, FG_DOC_PURPOSE, FG_FILENAME, FG_FILETYPE, FG_FILESIZE, FG_FILE_CREATED_ON) VALUES (:ID, :REFERENCE_ID, :APPLICANT_PARTY_CODE, :APPLICANT_REF_ID, :ISSUING_PARTY_CODE, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :VERSION_ID, :IS_MASTER_VERSION, :PROCESS_ID, :UPLOAD_REF_ID, :FG_FILE_TITLE, :FG_DOC_PURPOSE, :FG_FILENAME, :FG_FILETYPE, :FG_FILESIZE, :FG_FILE_CREATED_ON)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() FgCaseAttachment entity);

    @SqlQuery("UPDATE FG_CASE_ATTACHMENT SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, APPLICANT_PARTY_CODE = :APPLICANT_PARTY_CODE, APPLICANT_REF_ID = :APPLICANT_REF_ID, ISSUING_PARTY_CODE = :ISSUING_PARTY_CODE, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, VERSION_ID = :VERSION_ID, IS_MASTER_VERSION = :IS_MASTER_VERSION, PROCESS_ID = :PROCESS_ID, UPLOAD_REF_ID = :UPLOAD_REF_ID, FG_FILE_TITLE = :FG_FILE_TITLE, FG_DOC_PURPOSE = :FG_DOC_PURPOSE, FG_FILENAME = :FG_FILENAME, FG_FILETYPE = :FG_FILETYPE, FG_FILESIZE = :FG_FILESIZE, FG_FILE_CREATED_ON = :FG_FILE_CREATED_ON WHERE id = :id")
    public int update(@BindBean() FgCaseAttachment entity);

    @SqlQuery("DELETE FROM FG_CASE_ATTACHMENT WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
