package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.ScfFileLines;
import com.bsit.codegeneration.mapper.ScfFileLinesMapper;
import java.util.*;

@RegisterRowMapper(ScfFileLinesMapper.class)
public interface ScfFileLinesDao {

    @SqlQuery("SELECT * FROM SCF_FILE_LINES")
    public List<ScfFileLines> findAll();

    @SqlQuery("SELECT * FROM SCF_FILE_LINES WHERE id = :id")
    public Optional<ScfFileLines> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO SCF_FILE_LINES(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, ANCHOR_CRN, FILE_MASTER_REF_NO, LINE_NO, LINE_CONTENT, LINE_CONTENT_HASH_VALUE, LINE_RECORD_TYPE, LINE_STATUS, LINE_STATUS_REMARKS, LINE_HASH_VAL_STATUS, LINE_HASH_VAL_REMARKS, FILE_EVENT_REF_NO) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :ANCHOR_CRN, :FILE_MASTER_REF_NO, :LINE_NO, :LINE_CONTENT, :LINE_CONTENT_HASH_VALUE, :LINE_RECORD_TYPE, :LINE_STATUS, :LINE_STATUS_REMARKS, :LINE_HASH_VAL_STATUS, :LINE_HASH_VAL_REMARKS, :FILE_EVENT_REF_NO)")
    @GetGeneratedKeys()
    public String insert(@BindBean() ScfFileLines entity);

    @SqlQuery("UPDATE SCF_FILE_LINES SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, ANCHOR_CRN = :ANCHOR_CRN, FILE_MASTER_REF_NO = :FILE_MASTER_REF_NO, LINE_NO = :LINE_NO, LINE_CONTENT = :LINE_CONTENT, LINE_CONTENT_HASH_VALUE = :LINE_CONTENT_HASH_VALUE, LINE_RECORD_TYPE = :LINE_RECORD_TYPE, LINE_STATUS = :LINE_STATUS, LINE_STATUS_REMARKS = :LINE_STATUS_REMARKS, LINE_HASH_VAL_STATUS = :LINE_HASH_VAL_STATUS, LINE_HASH_VAL_REMARKS = :LINE_HASH_VAL_REMARKS, FILE_EVENT_REF_NO = :FILE_EVENT_REF_NO WHERE id = :id")
    public int update(@BindBean() ScfFileLines entity);

    @SqlQuery("DELETE FROM SCF_FILE_LINES WHERE id = :id")
    public int delete(@Bind("id") String id);
}
