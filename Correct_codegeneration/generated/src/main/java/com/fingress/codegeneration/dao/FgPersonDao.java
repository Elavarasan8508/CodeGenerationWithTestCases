package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgPerson;
import com.bsit.codegeneration.mapper.FgPersonMapper;
import java.util.*;

@RegisterRowMapper(FgPersonMapper.class)
public interface FgPersonDao {

    @SqlQuery("SELECT * FROM FG_PERSON")
    public List<FgPerson> findAll();

    @SqlQuery("SELECT * FROM FG_PERSON WHERE id = :id")
    public Optional<FgPerson> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO FG_PERSON(ID, BIRTH_COUNTRY_CODE, BIRTH_NAME, CITY_OF_BIRTH, DOMICILE_COUNTRY_CODE, GENDER_CODE, GIVEN_NAME, LANGUAGE_CODE, MIDDLE_NAME, MINOR_INDICATOR, NAME, NAME_PREFIX_CODE, NAME_SUFFIX, NATIONALITY_CODE, PROFESSION, PROVINCE_OF_BIRTH, RESIDENCE_COUNTRY_CODE, RESIDENTIAL_STATUS_CODE, TAXATION_COUNTRY_CODE, PARTY_ID, PARTY_VERSION_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, EMPLOYEE_NO, DATE_OF_BIRTH, DATE_OF_JOINING, DESIGNATION) VALUES (:ID, :BIRTH_COUNTRY_CODE, :BIRTH_NAME, :CITY_OF_BIRTH, :DOMICILE_COUNTRY_CODE, :GENDER_CODE, :GIVEN_NAME, :LANGUAGE_CODE, :MIDDLE_NAME, :MINOR_INDICATOR, :NAME, :NAME_PREFIX_CODE, :NAME_SUFFIX, :NATIONALITY_CODE, :PROFESSION, :PROVINCE_OF_BIRTH, :RESIDENCE_COUNTRY_CODE, :RESIDENTIAL_STATUS_CODE, :TAXATION_COUNTRY_CODE, :PARTY_ID, :PARTY_VERSION_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :EMPLOYEE_NO, :DATE_OF_BIRTH, :DATE_OF_JOINING, :DESIGNATION)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() FgPerson entity);

    @SqlQuery("UPDATE FG_PERSON SET ID = :ID, BIRTH_COUNTRY_CODE = :BIRTH_COUNTRY_CODE, BIRTH_NAME = :BIRTH_NAME, CITY_OF_BIRTH = :CITY_OF_BIRTH, DOMICILE_COUNTRY_CODE = :DOMICILE_COUNTRY_CODE, GENDER_CODE = :GENDER_CODE, GIVEN_NAME = :GIVEN_NAME, LANGUAGE_CODE = :LANGUAGE_CODE, MIDDLE_NAME = :MIDDLE_NAME, MINOR_INDICATOR = :MINOR_INDICATOR, NAME = :NAME, NAME_PREFIX_CODE = :NAME_PREFIX_CODE, NAME_SUFFIX = :NAME_SUFFIX, NATIONALITY_CODE = :NATIONALITY_CODE, PROFESSION = :PROFESSION, PROVINCE_OF_BIRTH = :PROVINCE_OF_BIRTH, RESIDENCE_COUNTRY_CODE = :RESIDENCE_COUNTRY_CODE, RESIDENTIAL_STATUS_CODE = :RESIDENTIAL_STATUS_CODE, TAXATION_COUNTRY_CODE = :TAXATION_COUNTRY_CODE, PARTY_ID = :PARTY_ID, PARTY_VERSION_ID = :PARTY_VERSION_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, EMPLOYEE_NO = :EMPLOYEE_NO, DATE_OF_BIRTH = :DATE_OF_BIRTH, DATE_OF_JOINING = :DATE_OF_JOINING, DESIGNATION = :DESIGNATION WHERE id = :id")
    public int update(@BindBean() FgPerson entity);

    @SqlQuery("DELETE FROM FG_PERSON WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
