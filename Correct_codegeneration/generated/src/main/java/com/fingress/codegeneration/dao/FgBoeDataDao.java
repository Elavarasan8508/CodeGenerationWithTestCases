package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgBoeData;
import com.bsit.codegeneration.mapper.FgBoeDataMapper;
import java.util.*;

@RegisterRowMapper(FgBoeDataMapper.class)
public interface FgBoeDataDao {

    @SqlQuery("SELECT * FROM FG_BOE_DATA")
    public List<FgBoeData> findAll();

    @SqlQuery("SELECT * FROM FG_BOE_DATA WHERE id = :id")
    public Optional<FgBoeData> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_BOE_DATA(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, BOE_NUMBER, BOE_DATE, PORT_OF_DIS, IMPORT_AGENCY, IE_CODE, OVERDUE_PERIOD, INVOICE_SL_NO, INV_NUM, TERMS_OF_INV, INV_CCY, INV_AMT, SUPPLIER_NAME, SUPPLIER_ADDR, SUPPLIER_COUNTRY, SELLER_NAME, SELLER_ADDR, SELLER_COUNTRY, UTILIZED_INV_AMT, OS_INV_AMT) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :BOE_NUMBER, :BOE_DATE, :PORT_OF_DIS, :IMPORT_AGENCY, :IE_CODE, :OVERDUE_PERIOD, :INVOICE_SL_NO, :INV_NUM, :TERMS_OF_INV, :INV_CCY, :INV_AMT, :SUPPLIER_NAME, :SUPPLIER_ADDR, :SUPPLIER_COUNTRY, :SELLER_NAME, :SELLER_ADDR, :SELLER_COUNTRY, :UTILIZED_INV_AMT, :OS_INV_AMT)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgBoeData entity);

    @SqlQuery("UPDATE FG_BOE_DATA SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, BOE_NUMBER = :BOE_NUMBER, BOE_DATE = :BOE_DATE, PORT_OF_DIS = :PORT_OF_DIS, IMPORT_AGENCY = :IMPORT_AGENCY, IE_CODE = :IE_CODE, OVERDUE_PERIOD = :OVERDUE_PERIOD, INVOICE_SL_NO = :INVOICE_SL_NO, INV_NUM = :INV_NUM, TERMS_OF_INV = :TERMS_OF_INV, INV_CCY = :INV_CCY, INV_AMT = :INV_AMT, SUPPLIER_NAME = :SUPPLIER_NAME, SUPPLIER_ADDR = :SUPPLIER_ADDR, SUPPLIER_COUNTRY = :SUPPLIER_COUNTRY, SELLER_NAME = :SELLER_NAME, SELLER_ADDR = :SELLER_ADDR, SELLER_COUNTRY = :SELLER_COUNTRY, UTILIZED_INV_AMT = :UTILIZED_INV_AMT, OS_INV_AMT = :OS_INV_AMT WHERE id = :id")
    public int update(@BindBean() FgBoeData entity);

    @SqlQuery("DELETE FROM FG_BOE_DATA WHERE id = :id")
    public int delete(@Bind("id") String id);
}
