package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgBatch;
import com.bsit.codegeneration.mapper.FgBatchMapper;
import java.util.*;

@RegisterRowMapper(FgBatchMapper.class)
public interface FgBatchDao {

    @SqlQuery("SELECT * FROM FG_BATCH")
    public List<FgBatch> findAll();

    @SqlQuery("SELECT * FROM FG_BATCH WHERE id = :id")
    public Optional<FgBatch> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_BATCH(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, RECORD_IDENTIFIER, TOT_INSTRUMENTS, TOTAL_AMOUNT, BATCH_REF_NO, BATCH_DATE, PRODUCT_CODE, UPLOAD_REF_ID, PMNT_HDR_IDENTIFIER, PMNT_DATE_FOR_FILE, COUNT_OF_INSTRUCTION) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :RECORD_IDENTIFIER, :TOT_INSTRUMENTS, :TOTAL_AMOUNT, :BATCH_REF_NO, :BATCH_DATE, :PRODUCT_CODE, :UPLOAD_REF_ID, :PMNT_HDR_IDENTIFIER, :PMNT_DATE_FOR_FILE, :COUNT_OF_INSTRUCTION)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgBatch entity);

    @SqlQuery("UPDATE FG_BATCH SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, RECORD_IDENTIFIER = :RECORD_IDENTIFIER, TOT_INSTRUMENTS = :TOT_INSTRUMENTS, TOTAL_AMOUNT = :TOTAL_AMOUNT, BATCH_REF_NO = :BATCH_REF_NO, BATCH_DATE = :BATCH_DATE, PRODUCT_CODE = :PRODUCT_CODE, UPLOAD_REF_ID = :UPLOAD_REF_ID, PMNT_HDR_IDENTIFIER = :PMNT_HDR_IDENTIFIER, PMNT_DATE_FOR_FILE = :PMNT_DATE_FOR_FILE, COUNT_OF_INSTRUCTION = :COUNT_OF_INSTRUCTION WHERE id = :id")
    public int update(@BindBean() FgBatch entity);

    @SqlQuery("DELETE FROM FG_BATCH WHERE id = :id")
    public int delete(@Bind("id") String id);
}
