package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgSdkProvisioningTemplate;
import com.bsit.codegeneration.mapper.FgSdkProvisioningTemplateMapper;
import java.util.*;

@RegisterRowMapper(FgSdkProvisioningTemplateMapper.class)
public interface FgSdkProvisioningTemplateDao {

    @SqlQuery("SELECT * FROM FG_SDK_PROVISIONING_TEMPLATE")
    public List<FgSdkProvisioningTemplate> findAll();

    @SqlQuery("SELECT * FROM FG_SDK_PROVISIONING_TEMPLATE WHERE id = :id")
    public Optional<FgSdkProvisioningTemplate> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_SDK_PROVISIONING_TEMPLATE(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, UPLOAD_REF_ID, ATTACHMENT_ID, PARENT_ID, PARENT_REF_ID, PARENT_VERSION_ID, IS_TEMPLATE, TEMPLATE_NAME, TEMPLATE_CODE, TEMPLATE_DESCRIPTION, TEMPLATE_TYPE, RULE_TEMPLATE, MODEL_SUBTYPE_CODE, ACTION_TYPE) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :UPLOAD_REF_ID, :ATTACHMENT_ID, :PARENT_ID, :PARENT_REF_ID, :PARENT_VERSION_ID, :IS_TEMPLATE, :TEMPLATE_NAME, :TEMPLATE_CODE, :TEMPLATE_DESCRIPTION, :TEMPLATE_TYPE, :RULE_TEMPLATE, :MODEL_SUBTYPE_CODE, :ACTION_TYPE)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgSdkProvisioningTemplate entity);

    @SqlQuery("UPDATE FG_SDK_PROVISIONING_TEMPLATE SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, UPLOAD_REF_ID = :UPLOAD_REF_ID, ATTACHMENT_ID = :ATTACHMENT_ID, PARENT_ID = :PARENT_ID, PARENT_REF_ID = :PARENT_REF_ID, PARENT_VERSION_ID = :PARENT_VERSION_ID, IS_TEMPLATE = :IS_TEMPLATE, TEMPLATE_NAME = :TEMPLATE_NAME, TEMPLATE_CODE = :TEMPLATE_CODE, TEMPLATE_DESCRIPTION = :TEMPLATE_DESCRIPTION, TEMPLATE_TYPE = :TEMPLATE_TYPE, RULE_TEMPLATE = :RULE_TEMPLATE, MODEL_SUBTYPE_CODE = :MODEL_SUBTYPE_CODE, ACTION_TYPE = :ACTION_TYPE WHERE id = :id")
    public int update(@BindBean() FgSdkProvisioningTemplate entity);

    @SqlQuery("DELETE FROM FG_SDK_PROVISIONING_TEMPLATE WHERE id = :id")
    public int delete(@Bind("id") String id);
}
