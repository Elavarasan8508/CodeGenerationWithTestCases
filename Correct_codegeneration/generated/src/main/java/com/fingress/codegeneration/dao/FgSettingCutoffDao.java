package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgSettingCutoff;
import com.bsit.codegeneration.mapper.FgSettingCutoffMapper;
import java.util.*;

@RegisterRowMapper(FgSettingCutoffMapper.class)
public interface FgSettingCutoffDao {

    @SqlQuery("SELECT * FROM FG_SETTING_CUTOFF")
    public List<FgSettingCutoff> findAll();

    @SqlQuery("SELECT * FROM FG_SETTING_CUTOFF WHERE id = :id")
    public Optional<FgSettingCutoff> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_SETTING_CUTOFF(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, IS_TEMPLATE, SCOPE_TYPE, PRODUCT_CODE, SCOPE_LEVEL, SCOPE_VALUE, SUB_PRODUCT_CODE, VERSION_ID, IS_MASTER_VERSION, RATE_TYPE, ENTITY_CODE, UPLOAD_REF_ID, FG_GROUP_SCOPE, FG_ROLE_SCOPE, CORPORATE_CRN, ENTITY_NAME, BANK) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :IS_TEMPLATE, :SCOPE_TYPE, :PRODUCT_CODE, :SCOPE_LEVEL, :SCOPE_VALUE, :SUB_PRODUCT_CODE, :VERSION_ID, :IS_MASTER_VERSION, :RATE_TYPE, :ENTITY_CODE, :UPLOAD_REF_ID, :FG_GROUP_SCOPE, :FG_ROLE_SCOPE, :CORPORATE_CRN, :ENTITY_NAME, :BANK)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgSettingCutoff entity);

    @SqlQuery("UPDATE FG_SETTING_CUTOFF SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, IS_TEMPLATE = :IS_TEMPLATE, SCOPE_TYPE = :SCOPE_TYPE, PRODUCT_CODE = :PRODUCT_CODE, SCOPE_LEVEL = :SCOPE_LEVEL, SCOPE_VALUE = :SCOPE_VALUE, SUB_PRODUCT_CODE = :SUB_PRODUCT_CODE, VERSION_ID = :VERSION_ID, IS_MASTER_VERSION = :IS_MASTER_VERSION, RATE_TYPE = :RATE_TYPE, ENTITY_CODE = :ENTITY_CODE, UPLOAD_REF_ID = :UPLOAD_REF_ID, FG_GROUP_SCOPE = :FG_GROUP_SCOPE, FG_ROLE_SCOPE = :FG_ROLE_SCOPE, CORPORATE_CRN = :CORPORATE_CRN, ENTITY_NAME = :ENTITY_NAME, BANK = :BANK WHERE id = :id")
    public int update(@BindBean() FgSettingCutoff entity);

    @SqlQuery("DELETE FROM FG_SETTING_CUTOFF WHERE id = :id")
    public int delete(@Bind("id") String id);
}
