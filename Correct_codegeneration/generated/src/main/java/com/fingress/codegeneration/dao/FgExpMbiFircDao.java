import java.util.*;

@RegisterRowMapper(FgExpMbiFircMapper.class)
public interface FgExpMbiFircDao {

    @SqlQuery("SELECT * FROM FG_EXP_MBI_FIRC")
    public List<FgExpMbiFirc> findAll();

    @SqlQuery("SELECT * FROM FG_EXP_MBI_FIRC WHERE id = :id")
    public Optional<FgExpMbiFirc> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_EXP_MBI_FIRC(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, FIRC_NUMBER, AD_CODE, FIRC_AMT, FIRC_UTIL_AMT, FIRC_OS_AMT, FIRC_CCY, FIRC_DATE, IE_CODE, REMITTER_NAME, REMITTER_COUNTRY, REMIT_BANK_COUNTRY, BILL_NO, PARENT_REF_ID, PARENT_VERSION_ID, SETTLED_AMT) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :FIRC_NUMBER, :AD_CODE, :FIRC_AMT, :FIRC_UTIL_AMT, :FIRC_OS_AMT, :FIRC_CCY, :FIRC_DATE, :IE_CODE, :REMITTER_NAME, :REMITTER_COUNTRY, :REMIT_BANK_COUNTRY, :BILL_NO, :PARENT_REF_ID, :PARENT_VERSION_ID, :SETTLED_AMT)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgExpMbiFirc entity);

    @SqlQuery("UPDATE FG_EXP_MBI_FIRC SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, FIRC_NUMBER = :FIRC_NUMBER, AD_CODE = :AD_CODE, FIRC_AMT = :FIRC_AMT, FIRC_UTIL_AMT = :FIRC_UTIL_AMT, FIRC_OS_AMT = :FIRC_OS_AMT, FIRC_CCY = :FIRC_CCY, FIRC_DATE = :FIRC_DATE, IE_CODE = :IE_CODE, REMITTER_NAME = :REMITTER_NAME, REMITTER_COUNTRY = :REMITTER_COUNTRY, REMIT_BANK_COUNTRY = :REMIT_BANK_COUNTRY, BILL_NO = :BILL_NO, PARENT_REF_ID = :PARENT_REF_ID, PARENT_VERSION_ID = :PARENT_VERSION_ID, SETTLED_AMT = :SETTLED_AMT WHERE id = :id")
    public int update(@BindBean() FgExpMbiFirc entity);

    @SqlQuery("DELETE FROM FG_EXP_MBI_FIRC WHERE id = :id")
    public int delete(@Bind("id") String id);
}
