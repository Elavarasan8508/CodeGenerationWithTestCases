package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgTrdPostings;
import com.bsit.codegeneration.mapper.FgTrdPostingsMapper;
import java.util.*;

@RegisterRowMapper(FgTrdPostingsMapper.class)
public interface FgTrdPostingsDao {

    @SqlQuery("SELECT * FROM FG_TRD_POSTINGS")
    public List<FgTrdPostings> findAll();

    @SqlQuery("SELECT * FROM FG_TRD_POSTINGS WHERE id = :id")
    public Optional<FgTrdPostings> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_TRD_POSTINGS(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, CUSTOMER, NOSTRO_REFERENCE, PORTAL_REFERENCE, POSTING_REQUEST, STATUS, POSTING_RESPONSE, TI_MASTER_REF, TI_PRODUCT_CODE, THEIR_REFERENCE, PRODUCT, TI_STATUS, EVENT_SERIAL, EVENT_PREFIX, BILL_NO) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :CUSTOMER, :NOSTRO_REFERENCE, :PORTAL_REFERENCE, :POSTING_REQUEST, :STATUS, :POSTING_RESPONSE, :TI_MASTER_REF, :TI_PRODUCT_CODE, :THEIR_REFERENCE, :PRODUCT, :TI_STATUS, :EVENT_SERIAL, :EVENT_PREFIX, :BILL_NO)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgTrdPostings entity);

    @SqlQuery("UPDATE FG_TRD_POSTINGS SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, CUSTOMER = :CUSTOMER, NOSTRO_REFERENCE = :NOSTRO_REFERENCE, PORTAL_REFERENCE = :PORTAL_REFERENCE, POSTING_REQUEST = :POSTING_REQUEST, STATUS = :STATUS, POSTING_RESPONSE = :POSTING_RESPONSE, TI_MASTER_REF = :TI_MASTER_REF, TI_PRODUCT_CODE = :TI_PRODUCT_CODE, THEIR_REFERENCE = :THEIR_REFERENCE, PRODUCT = :PRODUCT, TI_STATUS = :TI_STATUS, EVENT_SERIAL = :EVENT_SERIAL, EVENT_PREFIX = :EVENT_PREFIX, BILL_NO = :BILL_NO WHERE id = :id")
    public int update(@BindBean() FgTrdPostings entity);

    @SqlQuery("DELETE FROM FG_TRD_POSTINGS WHERE id = :id")
    public int delete(@Bind("id") String id);
}
