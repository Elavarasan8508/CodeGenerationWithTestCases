package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgSdkBusinessCode;
import com.bsit.codegeneration.mapper.FgSdkBusinessCodeMapper;
import java.util.*;

@RegisterRowMapper(FgSdkBusinessCodeMapper.class)
public interface FgSdkBusinessCodeDao {

    @SqlQuery("SELECT * FROM FG_SDK_BUSINESS_CODE")
    public List<FgSdkBusinessCode> findAll();

    @SqlQuery("SELECT * FROM FG_SDK_BUSINESS_CODE WHERE id = :id")
    public Optional<FgSdkBusinessCode> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_SDK_BUSINESS_CODE(ID, REFERENCE_ID, APPLICANT_PARTY_CODE, APPLICANT_REF_ID, ISSUING_PARTY_CODE, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, CODE_TYPE, PARENT_TYPE, PARENT_CODE, TEMPLATE_CODE, DESCRIPTION, CODE_VALUE, NAME, ACTIVECODE, PARENT_REF_ID, PARENT_VERSION_ID) VALUES (:ID, :REFERENCE_ID, :APPLICANT_PARTY_CODE, :APPLICANT_REF_ID, :ISSUING_PARTY_CODE, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :CODE_TYPE, :PARENT_TYPE, :PARENT_CODE, :TEMPLATE_CODE, :DESCRIPTION, :CODE_VALUE, :NAME, :ACTIVECODE, :PARENT_REF_ID, :PARENT_VERSION_ID)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgSdkBusinessCode entity);

    @SqlQuery("UPDATE FG_SDK_BUSINESS_CODE SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, APPLICANT_PARTY_CODE = :APPLICANT_PARTY_CODE, APPLICANT_REF_ID = :APPLICANT_REF_ID, ISSUING_PARTY_CODE = :ISSUING_PARTY_CODE, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, CODE_TYPE = :CODE_TYPE, PARENT_TYPE = :PARENT_TYPE, PARENT_CODE = :PARENT_CODE, TEMPLATE_CODE = :TEMPLATE_CODE, DESCRIPTION = :DESCRIPTION, CODE_VALUE = :CODE_VALUE, NAME = :NAME, ACTIVECODE = :ACTIVECODE, PARENT_REF_ID = :PARENT_REF_ID, PARENT_VERSION_ID = :PARENT_VERSION_ID WHERE id = :id")
    public int update(@BindBean() FgSdkBusinessCode entity);

    @SqlQuery("DELETE FROM FG_SDK_BUSINESS_CODE WHERE id = :id")
    public int delete(@Bind("id") String id);
}
