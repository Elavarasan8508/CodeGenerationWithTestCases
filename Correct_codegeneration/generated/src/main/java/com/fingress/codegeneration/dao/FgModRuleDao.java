package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgModRule;
import com.bsit.codegeneration.mapper.FgModRuleMapper;
import java.util.*;

@RegisterRowMapper(FgModRuleMapper.class)
public interface FgModRuleDao {

    @SqlQuery("SELECT * FROM FG_MOD_RULE")
    public List<FgModRule> findAll();

    @SqlQuery("SELECT * FROM FG_MOD_RULE WHERE id = :id")
    public Optional<FgModRule> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO FG_MOD_RULE(ID, PARTY_TYPE_CODE, PARTY_SUB_TYPE_CODE, RULE_TYPE_CODE, PARTY_CODE, NAME, REFERENCE_ID, RULE_OBJECT, DESCRIPTION, ACTIVE_CODE, STATUS_CODE, DEPLOYMENT_ID, DEPLOYMENT_TIME, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TYPE_CODE, SUB_TYPE_CODE, CODE, STAGE_CODE, AUTHORISED_RULE, IS_SYNC, SYNC_TARGET_SUB_TYPE_CODE, SYNC_SOURCE_SUB_TYPE_CODE, LAST_SYNCED_ON, APPLICANT_PARTY, OWNER_PARTY_CODE) VALUES (:ID, :PARTY_TYPE_CODE, :PARTY_SUB_TYPE_CODE, :RULE_TYPE_CODE, :PARTY_CODE, :NAME, :REFERENCE_ID, :RULE_OBJECT, :DESCRIPTION, :ACTIVE_CODE, :STATUS_CODE, :DEPLOYMENT_ID, :DEPLOYMENT_TIME, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TYPE_CODE, :SUB_TYPE_CODE, :CODE, :STAGE_CODE, :AUTHORISED_RULE, :IS_SYNC, :SYNC_TARGET_SUB_TYPE_CODE, :SYNC_SOURCE_SUB_TYPE_CODE, :LAST_SYNCED_ON, :APPLICANT_PARTY, :OWNER_PARTY_CODE)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() FgModRule entity);

    @SqlQuery("UPDATE FG_MOD_RULE SET ID = :ID, PARTY_TYPE_CODE = :PARTY_TYPE_CODE, PARTY_SUB_TYPE_CODE = :PARTY_SUB_TYPE_CODE, RULE_TYPE_CODE = :RULE_TYPE_CODE, PARTY_CODE = :PARTY_CODE, NAME = :NAME, REFERENCE_ID = :REFERENCE_ID, RULE_OBJECT = :RULE_OBJECT, DESCRIPTION = :DESCRIPTION, ACTIVE_CODE = :ACTIVE_CODE, STATUS_CODE = :STATUS_CODE, DEPLOYMENT_ID = :DEPLOYMENT_ID, DEPLOYMENT_TIME = :DEPLOYMENT_TIME, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, CODE = :CODE, STAGE_CODE = :STAGE_CODE, AUTHORISED_RULE = :AUTHORISED_RULE, IS_SYNC = :IS_SYNC, SYNC_TARGET_SUB_TYPE_CODE = :SYNC_TARGET_SUB_TYPE_CODE, SYNC_SOURCE_SUB_TYPE_CODE = :SYNC_SOURCE_SUB_TYPE_CODE, LAST_SYNCED_ON = :LAST_SYNCED_ON, APPLICANT_PARTY = :APPLICANT_PARTY, OWNER_PARTY_CODE = :OWNER_PARTY_CODE WHERE id = :id")
    public int update(@BindBean() FgModRule entity);

    @SqlQuery("DELETE FROM FG_MOD_RULE WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
