package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgTrdFt;
import com.bsit.codegeneration.mapper.FgTrdFtMapper;
import java.util.*;

@RegisterRowMapper(FgTrdFtMapper.class)
public interface FgTrdFtDao {

    @SqlQuery("SELECT * FROM FG_TRD_FT")
    public List<FgTrdFt> findAll();

    @SqlQuery("SELECT * FROM FG_TRD_FT WHERE id = :id")
    public Optional<FgTrdFt> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_TRD_FT(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, CUR_CODE, LODGE_DATE, DRAWEE, OUTSTANDING_AMT, OUTSTANDING_AMT_SETTLED, DSP_AMT, DEDUCTION, REASON, PARENT_REF_ID, PARENT_VERSION_ID, IR_REFERENCE_ID, OCF_REF_ID, APPLICANT_PARTY, BILL_REF_ID) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :CUR_CODE, :LODGE_DATE, :DRAWEE, :OUTSTANDING_AMT, :OUTSTANDING_AMT_SETTLED, :DSP_AMT, :DEDUCTION, :REASON, :PARENT_REF_ID, :PARENT_VERSION_ID, :IR_REFERENCE_ID, :OCF_REF_ID, :APPLICANT_PARTY, :BILL_REF_ID)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgTrdFt entity);

    @SqlQuery("UPDATE FG_TRD_FT SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, CUR_CODE = :CUR_CODE, LODGE_DATE = :LODGE_DATE, DRAWEE = :DRAWEE, OUTSTANDING_AMT = :OUTSTANDING_AMT, OUTSTANDING_AMT_SETTLED = :OUTSTANDING_AMT_SETTLED, DSP_AMT = :DSP_AMT, DEDUCTION = :DEDUCTION, REASON = :REASON, PARENT_REF_ID = :PARENT_REF_ID, PARENT_VERSION_ID = :PARENT_VERSION_ID, IR_REFERENCE_ID = :IR_REFERENCE_ID, OCF_REF_ID = :OCF_REF_ID, APPLICANT_PARTY = :APPLICANT_PARTY, BILL_REF_ID = :BILL_REF_ID WHERE id = :id")
    public int update(@BindBean() FgTrdFt entity);

    @SqlQuery("DELETE FROM FG_TRD_FT WHERE id = :id")
    public int delete(@Bind("id") String id);
}
