package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgLmsChargeSchemes;
import com.bsit.codegeneration.mapper.FgLmsChargeSchemesMapper;
import java.util.*;

@RegisterRowMapper(FgLmsChargeSchemesMapper.class)
public interface FgLmsChargeSchemesDao {

    @SqlQuery("SELECT * FROM FG_LMS_CHARGE_SCHEMES")
    public List<FgLmsChargeSchemes> findAll();

    @SqlQuery("SELECT * FROM FG_LMS_CHARGE_SCHEMES WHERE id = :id")
    public Optional<FgLmsChargeSchemes> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_LMS_CHARGE_SCHEMES(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, IS_TEMPLATE, SCHEME_NAME, SCHEME_DESCRIPTION, PRODUCT_CODE, PRODUCT_DESCRIPTION, EFFECTIVE_DATE, EXPIRY_DATE, VERSION_ID, IS_MASTER_VERSION, PRODUCT, SUB_PRODUCT) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :IS_TEMPLATE, :SCHEME_NAME, :SCHEME_DESCRIPTION, :PRODUCT_CODE, :PRODUCT_DESCRIPTION, :EFFECTIVE_DATE, :EXPIRY_DATE, :VERSION_ID, :IS_MASTER_VERSION, :PRODUCT, :SUB_PRODUCT)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgLmsChargeSchemes entity);

    @SqlQuery("UPDATE FG_LMS_CHARGE_SCHEMES SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, IS_TEMPLATE = :IS_TEMPLATE, SCHEME_NAME = :SCHEME_NAME, SCHEME_DESCRIPTION = :SCHEME_DESCRIPTION, PRODUCT_CODE = :PRODUCT_CODE, PRODUCT_DESCRIPTION = :PRODUCT_DESCRIPTION, EFFECTIVE_DATE = :EFFECTIVE_DATE, EXPIRY_DATE = :EXPIRY_DATE, VERSION_ID = :VERSION_ID, IS_MASTER_VERSION = :IS_MASTER_VERSION, PRODUCT = :PRODUCT, SUB_PRODUCT = :SUB_PRODUCT WHERE id = :id")
    public int update(@BindBean() FgLmsChargeSchemes entity);

    @SqlQuery("DELETE FROM FG_LMS_CHARGE_SCHEMES WHERE id = :id")
    public int delete(@Bind("id") String id);
}
