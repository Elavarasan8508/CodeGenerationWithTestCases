package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgTrdLc;
import com.bsit.codegeneration.mapper.FgTrdLcMapper;
import java.util.*;

@RegisterRowMapper(FgTrdLcMapper.class)
public interface FgTrdLcDao {

    @SqlQuery("SELECT * FROM FG_TRD_LC")
    public List<FgTrdLc> findAll();

    @SqlQuery("SELECT * FROM FG_TRD_LC WHERE id = :id")
    public Optional<FgTrdLc> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_TRD_LC(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, KOTAK_BILL_REF_ID, ISS_BANK_LC_REF_ID, TENOR, CUR_CODE, LODGE_DATE, OUTSTANDING_AMT, DSP_AMT, DEDUCTION, REASON, PARENT_REF_ID, PARENT_VERSION_ID, IR_REFERENCE_ID, APPLICANT_NAME, ELC_REF_ID, APPLICANT_PARTY, BILL_REF_ID) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :KOTAK_BILL_REF_ID, :ISS_BANK_LC_REF_ID, :TENOR, :CUR_CODE, :LODGE_DATE, :OUTSTANDING_AMT, :DSP_AMT, :DEDUCTION, :REASON, :PARENT_REF_ID, :PARENT_VERSION_ID, :IR_REFERENCE_ID, :APPLICANT_NAME, :ELC_REF_ID, :APPLICANT_PARTY, :BILL_REF_ID)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgTrdLc entity);

    @SqlQuery("UPDATE FG_TRD_LC SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, KOTAK_BILL_REF_ID = :KOTAK_BILL_REF_ID, ISS_BANK_LC_REF_ID = :ISS_BANK_LC_REF_ID, TENOR = :TENOR, CUR_CODE = :CUR_CODE, LODGE_DATE = :LODGE_DATE, OUTSTANDING_AMT = :OUTSTANDING_AMT, DSP_AMT = :DSP_AMT, DEDUCTION = :DEDUCTION, REASON = :REASON, PARENT_REF_ID = :PARENT_REF_ID, PARENT_VERSION_ID = :PARENT_VERSION_ID, IR_REFERENCE_ID = :IR_REFERENCE_ID, APPLICANT_NAME = :APPLICANT_NAME, ELC_REF_ID = :ELC_REF_ID, APPLICANT_PARTY = :APPLICANT_PARTY, BILL_REF_ID = :BILL_REF_ID WHERE id = :id")
    public int update(@BindBean() FgTrdLc entity);

    @SqlQuery("DELETE FROM FG_TRD_LC WHERE id = :id")
    public int delete(@Bind("id") String id);
}
