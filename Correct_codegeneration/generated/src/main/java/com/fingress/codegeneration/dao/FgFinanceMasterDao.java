package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgFinanceMaster;
import com.bsit.codegeneration.mapper.FgFinanceMasterMapper;
import java.util.*;

@RegisterRowMapper(FgFinanceMasterMapper.class)
public interface FgFinanceMasterDao {

    @SqlQuery("SELECT * FROM FG_FINANCE_MASTER")
    public List<FgFinanceMaster> findAll();

    @SqlQuery("SELECT * FROM FG_FINANCE_MASTER WHERE id = :id")
    public Optional<FgFinanceMaster> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_FINANCE_MASTER(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, IS_TEMPLATE, FILE_CONTENT, RETRIGGER_COUNT, UPLOAD_REF_ID, FINANCE_FILE_NAME, APPLICANT_PARTY_CODE, SEND_FINANCE_FILE, IS_FINANCE_SENT, FILE_NAME, REMARKS, CUTOFF_TIME, BATCH_REF_ID, BATCH_ACTION) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :IS_TEMPLATE, :FILE_CONTENT, :RETRIGGER_COUNT, :UPLOAD_REF_ID, :FINANCE_FILE_NAME, :APPLICANT_PARTY_CODE, :SEND_FINANCE_FILE, :IS_FINANCE_SENT, :FILE_NAME, :REMARKS, :CUTOFF_TIME, :BATCH_REF_ID, :BATCH_ACTION)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgFinanceMaster entity);

    @SqlQuery("UPDATE FG_FINANCE_MASTER SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, IS_TEMPLATE = :IS_TEMPLATE, FILE_CONTENT = :FILE_CONTENT, RETRIGGER_COUNT = :RETRIGGER_COUNT, UPLOAD_REF_ID = :UPLOAD_REF_ID, FINANCE_FILE_NAME = :FINANCE_FILE_NAME, APPLICANT_PARTY_CODE = :APPLICANT_PARTY_CODE, SEND_FINANCE_FILE = :SEND_FINANCE_FILE, IS_FINANCE_SENT = :IS_FINANCE_SENT, FILE_NAME = :FILE_NAME, REMARKS = :REMARKS, CUTOFF_TIME = :CUTOFF_TIME, BATCH_REF_ID = :BATCH_REF_ID, BATCH_ACTION = :BATCH_ACTION WHERE id = :id")
    public int update(@BindBean() FgFinanceMaster entity);

    @SqlQuery("DELETE FROM FG_FINANCE_MASTER WHERE id = :id")
    public int delete(@Bind("id") String id);
}
