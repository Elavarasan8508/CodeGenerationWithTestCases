package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgAlsAccStatement;
import com.bsit.codegeneration.mapper.FgAlsAccStatementMapper;
import java.util.*;

@RegisterRowMapper(FgAlsAccStatementMapper.class)
public interface FgAlsAccStatementDao {

    @SqlQuery("SELECT * FROM FG_ALS_ACC_STATEMENT")
    public List<FgAlsAccStatement> findAll();

    @SqlQuery("SELECT * FROM FG_ALS_ACC_STATEMENT WHERE id = :id")
    public Optional<FgAlsAccStatement> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_ALS_ACC_STATEMENT(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, IS_TEMPLATE, TXN_TYPE, TRANSACTION_DATE, VIRTUAL_ACCOUNT_NUMBER, VALUE_DATE, REMARKS, PARTICULARS, CURRENCY, DEBIT_AMOUNT, CREDIT_AMOUNT, BALANCE, TXN_STATUS, LEDGER_TYPE, TI_THEIR_REFERENCE) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :IS_TEMPLATE, :TXN_TYPE, :TRANSACTION_DATE, :VIRTUAL_ACCOUNT_NUMBER, :VALUE_DATE, :REMARKS, :PARTICULARS, :CURRENCY, :DEBIT_AMOUNT, :CREDIT_AMOUNT, :BALANCE, :TXN_STATUS, :LEDGER_TYPE, :TI_THEIR_REFERENCE)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgAlsAccStatement entity);

    @SqlQuery("UPDATE FG_ALS_ACC_STATEMENT SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, IS_TEMPLATE = :IS_TEMPLATE, TXN_TYPE = :TXN_TYPE, TRANSACTION_DATE = :TRANSACTION_DATE, VIRTUAL_ACCOUNT_NUMBER = :VIRTUAL_ACCOUNT_NUMBER, VALUE_DATE = :VALUE_DATE, REMARKS = :REMARKS, PARTICULARS = :PARTICULARS, CURRENCY = :CURRENCY, DEBIT_AMOUNT = :DEBIT_AMOUNT, CREDIT_AMOUNT = :CREDIT_AMOUNT, BALANCE = :BALANCE, TXN_STATUS = :TXN_STATUS, LEDGER_TYPE = :LEDGER_TYPE, TI_THEIR_REFERENCE = :TI_THEIR_REFERENCE WHERE id = :id")
    public int update(@BindBean() FgAlsAccStatement entity);

    @SqlQuery("DELETE FROM FG_ALS_ACC_STATEMENT WHERE id = :id")
    public int delete(@Bind("id") String id);
}
