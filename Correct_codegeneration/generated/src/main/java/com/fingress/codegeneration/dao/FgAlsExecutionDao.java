package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgAlsExecution;
import com.bsit.codegeneration.mapper.FgAlsExecutionMapper;
import java.util.*;

@RegisterRowMapper(FgAlsExecutionMapper.class)
public interface FgAlsExecutionDao {

    @SqlQuery("SELECT * FROM FG_ALS_EXECUTION")
    public List<FgAlsExecution> findAll();

    @SqlQuery("SELECT * FROM FG_ALS_EXECUTION WHERE id = :id")
    public Optional<FgAlsExecution> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_ALS_EXECUTION(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, IS_TEMPLATE, CUSTOMER_CRN, ACCOUNT_NO, LAST_EXECUTION, STATUS, TOTAL_LOANS, PROCESSED_LOANS, ERROR_DESC, ACCOUNT_BALANCE, TOTAL_BALANCE, REASON, EXECUTION_START_TIME, EXECUTION_END_TIME, FINACLE_STATUS, ACCOUNT_STATUS, DEBIT_LOCK, CUSTOMER_NAME) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :IS_TEMPLATE, :CUSTOMER_CRN, :ACCOUNT_NO, :LAST_EXECUTION, :STATUS, :TOTAL_LOANS, :PROCESSED_LOANS, :ERROR_DESC, :ACCOUNT_BALANCE, :TOTAL_BALANCE, :REASON, :EXECUTION_START_TIME, :EXECUTION_END_TIME, :FINACLE_STATUS, :ACCOUNT_STATUS, :DEBIT_LOCK, :CUSTOMER_NAME)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgAlsExecution entity);

    @SqlQuery("UPDATE FG_ALS_EXECUTION SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, IS_TEMPLATE = :IS_TEMPLATE, CUSTOMER_CRN = :CUSTOMER_CRN, ACCOUNT_NO = :ACCOUNT_NO, LAST_EXECUTION = :LAST_EXECUTION, STATUS = :STATUS, TOTAL_LOANS = :TOTAL_LOANS, PROCESSED_LOANS = :PROCESSED_LOANS, ERROR_DESC = :ERROR_DESC, ACCOUNT_BALANCE = :ACCOUNT_BALANCE, TOTAL_BALANCE = :TOTAL_BALANCE, REASON = :REASON, EXECUTION_START_TIME = :EXECUTION_START_TIME, EXECUTION_END_TIME = :EXECUTION_END_TIME, FINACLE_STATUS = :FINACLE_STATUS, ACCOUNT_STATUS = :ACCOUNT_STATUS, DEBIT_LOCK = :DEBIT_LOCK, CUSTOMER_NAME = :CUSTOMER_NAME WHERE id = :id")
    public int update(@BindBean() FgAlsExecution entity);

    @SqlQuery("DELETE FROM FG_ALS_EXECUTION WHERE id = :id")
    public int delete(@Bind("id") String id);
}
