package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgSettingFlag;
import com.bsit.codegeneration.mapper.FgSettingFlagMapper;
import java.util.*;

@RegisterRowMapper(FgSettingFlagMapper.class)
public interface FgSettingFlagDao {

    @SqlQuery("SELECT * FROM FG_SETTING_FLAG")
    public List<FgSettingFlag> findAll();

    @SqlQuery("SELECT * FROM FG_SETTING_FLAG WHERE id = :id")
    public Optional<FgSettingFlag> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_SETTING_FLAG(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, SETTING_NAME, SETTING_KEY, SETTING_CODE, SETTING_DESCRIPTION, SETTING_TAG, OWNER_PARTY_CODE, ENTITY_SUB_TYPE, SETTING_ENVIRONMENT, APPLICATION_TYPE, PROPERTY_TYPE, SETTING_STATUS, SETTING_VALUE, SETTING_DEFAULT_VALUE, ENCRYPTIONFLAG, PROCESS_ID, DEPLOYMENT_STATUS, PROGRAM_SELECT) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :SETTING_NAME, :SETTING_KEY, :SETTING_CODE, :SETTING_DESCRIPTION, :SETTING_TAG, :OWNER_PARTY_CODE, :ENTITY_SUB_TYPE, :SETTING_ENVIRONMENT, :APPLICATION_TYPE, :PROPERTY_TYPE, :SETTING_STATUS, :SETTING_VALUE, :SETTING_DEFAULT_VALUE, :ENCRYPTIONFLAG, :PROCESS_ID, :DEPLOYMENT_STATUS, :PROGRAM_SELECT)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgSettingFlag entity);

    @SqlQuery("UPDATE FG_SETTING_FLAG SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, SETTING_NAME = :SETTING_NAME, SETTING_KEY = :SETTING_KEY, SETTING_CODE = :SETTING_CODE, SETTING_DESCRIPTION = :SETTING_DESCRIPTION, SETTING_TAG = :SETTING_TAG, OWNER_PARTY_CODE = :OWNER_PARTY_CODE, ENTITY_SUB_TYPE = :ENTITY_SUB_TYPE, SETTING_ENVIRONMENT = :SETTING_ENVIRONMENT, APPLICATION_TYPE = :APPLICATION_TYPE, PROPERTY_TYPE = :PROPERTY_TYPE, SETTING_STATUS = :SETTING_STATUS, SETTING_VALUE = :SETTING_VALUE, SETTING_DEFAULT_VALUE = :SETTING_DEFAULT_VALUE, ENCRYPTIONFLAG = :ENCRYPTIONFLAG, PROCESS_ID = :PROCESS_ID, DEPLOYMENT_STATUS = :DEPLOYMENT_STATUS, PROGRAM_SELECT = :PROGRAM_SELECT WHERE id = :id")
    public int update(@BindBean() FgSettingFlag entity);

    @SqlQuery("DELETE FROM FG_SETTING_FLAG WHERE id = :id")
    public int delete(@Bind("id") String id);
}
