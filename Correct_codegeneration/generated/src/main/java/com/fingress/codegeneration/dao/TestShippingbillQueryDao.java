package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.TestShippingbillQuery;
import com.bsit.codegeneration.mapper.TestShippingbillQueryMapper;
import java.util.*;

@RegisterRowMapper(TestShippingbillQueryMapper.class)
public interface TestShippingbillQueryDao {

    @SqlQuery("SELECT * FROM TEST_SHIPPINGBILL_QUERY")
    public List<TestShippingbillQuery> findAll();

    @SqlQuery("SELECT * FROM TEST_SHIPPINGBILL_QUERY WHERE id = :id")
    public Optional<TestShippingbillQuery> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO TEST_SHIPPINGBILL_QUERY(BILL_ID, INVOICE_ID, SHIPPING_BILL_STATUS, SHIPPINGBILL_NO, FORM_NO, SHIPPINGBILL_DATE, LEO_DATE, AD_CODE, PORT_OF_LOADING, EXPORT_AGENCY, EXPORT_TYPE, IE_CODE, AD_BILL_NUMBER, INVOICE_STATUS, INVOICE_NO, INVOICE_DATE, INVOICE_SERIAL_NO, FOB_CURRENCY, FRIEGHT_CURRENCY, INSURANCE_CURRENCY, FOB_AMOUNT, UTILIZED_FOB_AMOUNT, FREIGHT_AMOUNT, UTILIZED_FREIGHT_AMOUNT, INSURANCE_AMOUNT, UTILIZED_INSURANCE_AMOUNT) VALUES (:BILL_ID, :INVOICE_ID, :SHIPPING_BILL_STATUS, :SHIPPINGBILL_NO, :FORM_NO, :SHIPPINGBILL_DATE, :LEO_DATE, :AD_CODE, :PORT_OF_LOADING, :EXPORT_AGENCY, :EXPORT_TYPE, :IE_CODE, :AD_BILL_NUMBER, :INVOICE_STATUS, :INVOICE_NO, :INVOICE_DATE, :INVOICE_SERIAL_NO, :FOB_CURRENCY, :FRIEGHT_CURRENCY, :INSURANCE_CURRENCY, :FOB_AMOUNT, :UTILIZED_FOB_AMOUNT, :FREIGHT_AMOUNT, :UTILIZED_FREIGHT_AMOUNT, :INSURANCE_AMOUNT, :UTILIZED_INSURANCE_AMOUNT)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() TestShippingbillQuery entity);

    @SqlQuery("UPDATE TEST_SHIPPINGBILL_QUERY SET BILL_ID = :BILL_ID, INVOICE_ID = :INVOICE_ID, SHIPPING_BILL_STATUS = :SHIPPING_BILL_STATUS, SHIPPINGBILL_NO = :SHIPPINGBILL_NO, FORM_NO = :FORM_NO, SHIPPINGBILL_DATE = :SHIPPINGBILL_DATE, LEO_DATE = :LEO_DATE, AD_CODE = :AD_CODE, PORT_OF_LOADING = :PORT_OF_LOADING, EXPORT_AGENCY = :EXPORT_AGENCY, EXPORT_TYPE = :EXPORT_TYPE, IE_CODE = :IE_CODE, AD_BILL_NUMBER = :AD_BILL_NUMBER, INVOICE_STATUS = :INVOICE_STATUS, INVOICE_NO = :INVOICE_NO, INVOICE_DATE = :INVOICE_DATE, INVOICE_SERIAL_NO = :INVOICE_SERIAL_NO, FOB_CURRENCY = :FOB_CURRENCY, FRIEGHT_CURRENCY = :FRIEGHT_CURRENCY, INSURANCE_CURRENCY = :INSURANCE_CURRENCY, FOB_AMOUNT = :FOB_AMOUNT, UTILIZED_FOB_AMOUNT = :UTILIZED_FOB_AMOUNT, FREIGHT_AMOUNT = :FREIGHT_AMOUNT, UTILIZED_FREIGHT_AMOUNT = :UTILIZED_FREIGHT_AMOUNT, INSURANCE_AMOUNT = :INSURANCE_AMOUNT, UTILIZED_INSURANCE_AMOUNT = :UTILIZED_INSURANCE_AMOUNT WHERE id = :id")
    public int update(@BindBean() TestShippingbillQuery entity);

    @SqlQuery("DELETE FROM TEST_SHIPPINGBILL_QUERY WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
