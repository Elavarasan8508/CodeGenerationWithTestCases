package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgTiLoans;
import com.bsit.codegeneration.mapper.FgTiLoansMapper;
import java.util.*;

@RegisterRowMapper(FgTiLoansMapper.class)
public interface FgTiLoansDao {

    @SqlQuery("SELECT * FROM FG_TI_LOANS")
    public List<FgTiLoans> findAll();

    @SqlQuery("SELECT * FROM FG_TI_LOANS WHERE id = :id")
    public Optional<FgTiLoans> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_TI_LOANS(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, IS_TEMPLATE, CUST_MNM, DISB_DATE, INTEREST_REPAYMENT_AMOUNT, MASTER_REF, MATURITY_DATE, OVERDUE_Y_N, STATUS, VERSION_ID, IS_MASTER_VERSION, TOTAL_REPAYMENT_AMOUNT, PRINCIPLE_REPAYMENT_AMOUNT) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :IS_TEMPLATE, :CUST_MNM, :DISB_DATE, :INTEREST_REPAYMENT_AMOUNT, :MASTER_REF, :MATURITY_DATE, :OVERDUE_Y_N, :STATUS, :VERSION_ID, :IS_MASTER_VERSION, :TOTAL_REPAYMENT_AMOUNT, :PRINCIPLE_REPAYMENT_AMOUNT)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgTiLoans entity);

    @SqlQuery("UPDATE FG_TI_LOANS SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, IS_TEMPLATE = :IS_TEMPLATE, CUST_MNM = :CUST_MNM, DISB_DATE = :DISB_DATE, INTEREST_REPAYMENT_AMOUNT = :INTEREST_REPAYMENT_AMOUNT, MASTER_REF = :MASTER_REF, MATURITY_DATE = :MATURITY_DATE, OVERDUE_Y_N = :OVERDUE_Y_N, STATUS = :STATUS, VERSION_ID = :VERSION_ID, IS_MASTER_VERSION = :IS_MASTER_VERSION, TOTAL_REPAYMENT_AMOUNT = :TOTAL_REPAYMENT_AMOUNT, PRINCIPLE_REPAYMENT_AMOUNT = :PRINCIPLE_REPAYMENT_AMOUNT WHERE id = :id")
    public int update(@BindBean() FgTiLoans entity);

    @SqlQuery("DELETE FROM FG_TI_LOANS WHERE id = :id")
    public int delete(@Bind("id") String id);
}
