package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgTdOtpReq;
import com.bsit.codegeneration.mapper.FgTdOtpReqMapper;
import java.util.*;

@RegisterRowMapper(FgTdOtpReqMapper.class)
public interface FgTdOtpReqDao {

    @SqlQuery("SELECT * FROM FG_TD_OTP_REQ")
    public List<FgTdOtpReq> findAll();

    @SqlQuery("SELECT * FROM FG_TD_OTP_REQ WHERE id = :id")
    public Optional<FgTdOtpReq> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_TD_OTP_REQ(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, APPLICANT_PARTY, FULFILLMENT_PARTY, IS_TEMPLATE, OTP_COUNT, OTP_RESENT, OTP_FAILED_COUNT, OTP_SENT_TIME, OTP_LOCKED_TIME, OTP_UNLOCK_TIME, USER_CRN, ENTITY_TYPE_CODE, MOBILE_MASKED, EMAIL_MASKED) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :IS_TEMPLATE, :OTP_COUNT, :OTP_RESENT, :OTP_FAILED_COUNT, :OTP_SENT_TIME, :OTP_LOCKED_TIME, :OTP_UNLOCK_TIME, :USER_CRN, :ENTITY_TYPE_CODE, :MOBILE_MASKED, :EMAIL_MASKED)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgTdOtpReq entity);

    @SqlQuery("UPDATE FG_TD_OTP_REQ SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, IS_TEMPLATE = :IS_TEMPLATE, OTP_COUNT = :OTP_COUNT, OTP_RESENT = :OTP_RESENT, OTP_FAILED_COUNT = :OTP_FAILED_COUNT, OTP_SENT_TIME = :OTP_SENT_TIME, OTP_LOCKED_TIME = :OTP_LOCKED_TIME, OTP_UNLOCK_TIME = :OTP_UNLOCK_TIME, USER_CRN = :USER_CRN, ENTITY_TYPE_CODE = :ENTITY_TYPE_CODE, MOBILE_MASKED = :MOBILE_MASKED, EMAIL_MASKED = :EMAIL_MASKED WHERE id = :id")
    public int update(@BindBean() FgTdOtpReq entity);

    @SqlQuery("DELETE FROM FG_TD_OTP_REQ WHERE id = :id")
    public int delete(@Bind("id") String id);
}
