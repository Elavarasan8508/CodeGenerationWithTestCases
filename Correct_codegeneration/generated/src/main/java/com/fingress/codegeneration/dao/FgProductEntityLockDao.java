package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgProductEntityLock;
import com.bsit.codegeneration.mapper.FgProductEntityLockMapper;
import java.util.*;

@RegisterRowMapper(FgProductEntityLockMapper.class)
public interface FgProductEntityLockDao {

    @SqlQuery("SELECT * FROM FG_PRODUCT_ENTITY_LOCK")
    public List<FgProductEntityLock> findAll();

    @SqlQuery("SELECT * FROM FG_PRODUCT_ENTITY_LOCK WHERE id = :id")
    public Optional<FgProductEntityLock> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_PRODUCT_ENTITY_LOCK(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, USER_CODE, ORG_CODE, ENTITY_VERSION, ATTEMPT_COUNT, ACTION_CODE, LOCK_TIME, EXPIRY_TIME, USER_ROLE, LOCK_COUNT, ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE, ENTITY_REFERENCE_ID, RESEND_ATTEMPT_COUNT) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :USER_CODE, :ORG_CODE, :ENTITY_VERSION, :ATTEMPT_COUNT, :ACTION_CODE, :LOCK_TIME, :EXPIRY_TIME, :USER_ROLE, :LOCK_COUNT, :ENTITY_TYPE_CODE, :ENTITY_SUB_TYPE_CODE, :ENTITY_REFERENCE_ID, :RESEND_ATTEMPT_COUNT)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgProductEntityLock entity);

    @SqlQuery("UPDATE FG_PRODUCT_ENTITY_LOCK SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, USER_CODE = :USER_CODE, ORG_CODE = :ORG_CODE, ENTITY_VERSION = :ENTITY_VERSION, ATTEMPT_COUNT = :ATTEMPT_COUNT, ACTION_CODE = :ACTION_CODE, LOCK_TIME = :LOCK_TIME, EXPIRY_TIME = :EXPIRY_TIME, USER_ROLE = :USER_ROLE, LOCK_COUNT = :LOCK_COUNT, ENTITY_TYPE_CODE = :ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE = :ENTITY_SUB_TYPE_CODE, ENTITY_REFERENCE_ID = :ENTITY_REFERENCE_ID, RESEND_ATTEMPT_COUNT = :RESEND_ATTEMPT_COUNT WHERE id = :id")
    public int update(@BindBean() FgProductEntityLock entity);

    @SqlQuery("DELETE FROM FG_PRODUCT_ENTITY_LOCK WHERE id = :id")
    public int delete(@Bind("id") String id);
}
