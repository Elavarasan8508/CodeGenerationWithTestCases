package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.ScfFileEvents;
import com.bsit.codegeneration.mapper.ScfFileEventsMapper;
import java.util.*;

@RegisterRowMapper(ScfFileEventsMapper.class)
public interface ScfFileEventsDao {

    @SqlQuery("SELECT * FROM SCF_FILE_EVENTS")
    public List<ScfFileEvents> findAll();

    @SqlQuery("SELECT * FROM SCF_FILE_EVENTS WHERE id = :id")
    public Optional<ScfFileEvents> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO SCF_FILE_EVENTS(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, ANCHOR_CRN, EVENT_TYPE, EVENT_CODE, EVENT_NAME, EVENT_SOURCE, EVENT_TARGET, ENTITY_NAME, RECORD_COUNT, FILE_ATTACH_REF_NO, EVENT_STATUS, EVENT_REMARKS, FILE_NAME, FILE_MASTER_REF_NO, EVENT_BATCH_ID, DISPLAY_SOURCE, DISPLAY_TARGET, RETRIGGER_COUNT, EVENT_FINPAY_ID, FINTECH_SOURCE) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :ANCHOR_CRN, :EVENT_TYPE, :EVENT_CODE, :EVENT_NAME, :EVENT_SOURCE, :EVENT_TARGET, :ENTITY_NAME, :RECORD_COUNT, :FILE_ATTACH_REF_NO, :EVENT_STATUS, :EVENT_REMARKS, :FILE_NAME, :FILE_MASTER_REF_NO, :EVENT_BATCH_ID, :DISPLAY_SOURCE, :DISPLAY_TARGET, :RETRIGGER_COUNT, :EVENT_FINPAY_ID, :FINTECH_SOURCE)")
    @GetGeneratedKeys()
    public String insert(@BindBean() ScfFileEvents entity);

    @SqlQuery("UPDATE SCF_FILE_EVENTS SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, ANCHOR_CRN = :ANCHOR_CRN, EVENT_TYPE = :EVENT_TYPE, EVENT_CODE = :EVENT_CODE, EVENT_NAME = :EVENT_NAME, EVENT_SOURCE = :EVENT_SOURCE, EVENT_TARGET = :EVENT_TARGET, ENTITY_NAME = :ENTITY_NAME, RECORD_COUNT = :RECORD_COUNT, FILE_ATTACH_REF_NO = :FILE_ATTACH_REF_NO, EVENT_STATUS = :EVENT_STATUS, EVENT_REMARKS = :EVENT_REMARKS, FILE_NAME = :FILE_NAME, FILE_MASTER_REF_NO = :FILE_MASTER_REF_NO, EVENT_BATCH_ID = :EVENT_BATCH_ID, DISPLAY_SOURCE = :DISPLAY_SOURCE, DISPLAY_TARGET = :DISPLAY_TARGET, RETRIGGER_COUNT = :RETRIGGER_COUNT, EVENT_FINPAY_ID = :EVENT_FINPAY_ID, FINTECH_SOURCE = :FINTECH_SOURCE WHERE id = :id")
    public int update(@BindBean() ScfFileEvents entity);

    @SqlQuery("DELETE FROM SCF_FILE_EVENTS WHERE id = :id")
    public int delete(@Bind("id") String id);
}
