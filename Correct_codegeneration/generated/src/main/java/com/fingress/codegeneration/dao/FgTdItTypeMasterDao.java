import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgTdItTypeMaster;
import com.bsit.codegeneration.mapper.FgTdItTypeMasterMapper;
import java.util.*;

@RegisterRowMapper(FgTdItTypeMasterMapper.class)
public interface FgTdItTypeMasterDao {

    @SqlQuery("SELECT * FROM FG_TD_IT_TYPE_MASTER")
    public List<FgTdItTypeMaster> findAll();

    @SqlQuery("SELECT * FROM FG_TD_IT_TYPE_MASTER WHERE id = :id")
    public Optional<FgTdItTypeMaster> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_TD_IT_TYPE_MASTER(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, PROCESS_ID, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, IT_TYPE, CUST_CLASSIFICATION, TYPE, DESCRIPTION, APPLICANT_PARTY, FULFILLMENT_PARTY, VERSION_ID, IS_MASTER_VERSION, TYPE_DUMMY, TASK_REASON, PARENT_VERSION_ID, PARENT_REF_ID, ORIGIN) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :PROCESS_ID, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :IT_TYPE, :CUST_CLASSIFICATION, :TYPE, :DESCRIPTION, :APPLICANT_PARTY, :FULFILLMENT_PARTY, :VERSION_ID, :IS_MASTER_VERSION, :TYPE_DUMMY, :TASK_REASON, :PARENT_VERSION_ID, :PARENT_REF_ID, :ORIGIN)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgTdItTypeMaster entity);

    @SqlQuery("UPDATE FG_TD_IT_TYPE_MASTER SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, PROCESS_ID = :PROCESS_ID, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, IT_TYPE = :IT_TYPE, CUST_CLASSIFICATION = :CUST_CLASSIFICATION, TYPE = :TYPE, DESCRIPTION = :DESCRIPTION, APPLICANT_PARTY = :APPLICANT_PARTY, FULFILLMENT_PARTY = :FULFILLMENT_PARTY, VERSION_ID = :VERSION_ID, IS_MASTER_VERSION = :IS_MASTER_VERSION, TYPE_DUMMY = :TYPE_DUMMY, TASK_REASON = :TASK_REASON, PARENT_VERSION_ID = :PARENT_VERSION_ID, PARENT_REF_ID = :PARENT_REF_ID, ORIGIN = :ORIGIN WHERE id = :id")
    public int update(@BindBean() FgTdItTypeMaster entity);

    @SqlQuery("DELETE FROM FG_TD_IT_TYPE_MASTER WHERE id = :id")
    public int delete(@Bind("id") String id);
}
