package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgProductStageHistory;
import com.bsit.codegeneration.mapper.FgProductStageHistoryMapper;
import java.util.*;

@RegisterRowMapper(FgProductStageHistoryMapper.class)
public interface FgProductStageHistoryDao {

    @SqlQuery("SELECT * FROM FG_PRODUCT_STAGE_HISTORY")
    public List<FgProductStageHistory> findAll();

    @SqlQuery("SELECT * FROM FG_PRODUCT_STAGE_HISTORY WHERE id = :id")
    public Optional<FgProductStageHistory> findById(@Bind("id") String id);

    @SqlUpdate("INSERT INTO FG_PRODUCT_STAGE_HISTORY(ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE, WF_PROCESS_ID, WF_DEFINITION_ID, ENTITY_REF_ID, ENTITY_VERSION_NO, STAGE_CODE, PERF_PTY_SUBTYP_CODE, PERF_PTY_TYP_CODE, PARTY_CODE, PERF_PTY_ROLE_CODE, STP, STAGE_ORDER_IDX, STAGE_START_TIME, STAGE_END_TIME, WF_PROCESS_STATUS_CODE, ACTIVE_CODE, STATUS_CODE, CREATED_BY, CREATED_ON, LAST_UPDATED_BY, LAST_UPDATED_ON, LAST_AUTHORISED_BY, LAST_AUTHORISED_ON, PERF_PTY_FIELD_CODE, UPLOAD_REF_ID, ID) VALUES (:ENTITY_TYPE_CODE, :ENTITY_SUB_TYPE_CODE, :WF_PROCESS_ID, :WF_DEFINITION_ID, :ENTITY_REF_ID, :ENTITY_VERSION_NO, :STAGE_CODE, :PERF_PTY_SUBTYP_CODE, :PERF_PTY_TYP_CODE, :PARTY_CODE, :PERF_PTY_ROLE_CODE, :STP, :STAGE_ORDER_IDX, :STAGE_START_TIME, :STAGE_END_TIME, :WF_PROCESS_STATUS_CODE, :ACTIVE_CODE, :STATUS_CODE, :CREATED_BY, :CREATED_ON, :LAST_UPDATED_BY, :LAST_UPDATED_ON, :LAST_AUTHORISED_BY, :LAST_AUTHORISED_ON, :PERF_PTY_FIELD_CODE, :UPLOAD_REF_ID, :ID)")
    @GetGeneratedKeys()
    public String insert(@BindBean() FgProductStageHistory entity);

    @SqlQuery("UPDATE FG_PRODUCT_STAGE_HISTORY SET ENTITY_TYPE_CODE = :ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE = :ENTITY_SUB_TYPE_CODE, WF_PROCESS_ID = :WF_PROCESS_ID, WF_DEFINITION_ID = :WF_DEFINITION_ID, ENTITY_REF_ID = :ENTITY_REF_ID, ENTITY_VERSION_NO = :ENTITY_VERSION_NO, STAGE_CODE = :STAGE_CODE, PERF_PTY_SUBTYP_CODE = :PERF_PTY_SUBTYP_CODE, PERF_PTY_TYP_CODE = :PERF_PTY_TYP_CODE, PARTY_CODE = :PARTY_CODE, PERF_PTY_ROLE_CODE = :PERF_PTY_ROLE_CODE, STP = :STP, STAGE_ORDER_IDX = :STAGE_ORDER_IDX, STAGE_START_TIME = :STAGE_START_TIME, STAGE_END_TIME = :STAGE_END_TIME, WF_PROCESS_STATUS_CODE = :WF_PROCESS_STATUS_CODE, ACTIVE_CODE = :ACTIVE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_BY = :CREATED_BY, CREATED_ON = :CREATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, PERF_PTY_FIELD_CODE = :PERF_PTY_FIELD_CODE, UPLOAD_REF_ID = :UPLOAD_REF_ID, ID = :ID WHERE id = :id")
    public int update(@BindBean() FgProductStageHistory entity);

    @SqlQuery("DELETE FROM FG_PRODUCT_STAGE_HISTORY WHERE id = :id")
    public int delete(@Bind("id") String id);
}
