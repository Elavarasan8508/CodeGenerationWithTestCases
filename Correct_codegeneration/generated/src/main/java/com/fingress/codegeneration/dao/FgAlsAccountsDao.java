package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgAlsAccounts;
import com.bsit.codegeneration.mapper.FgAlsAccountsMapper;
import java.util.*;

@RegisterRowMapper(FgAlsAccountsMapper.class)
public interface FgAlsAccountsDao {

    @SqlQuery("SELECT * FROM FG_ALS_ACCOUNTS")
    public List<FgAlsAccounts> findAll();

    @SqlQuery("SELECT * FROM FG_ALS_ACCOUNTS WHERE id = :id")
    public Optional<FgAlsAccounts> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO FG_ALS_ACCOUNTS(ID, REFERENCE_ID, TYPE_CODE, SUB_TYPE_CODE, ACTIVE_CODE, STAGE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, TEMPLATE, IS_TEMPLATE, ACCOUNT_NUMBER, PRODUCT_CODE, LOGIC_OF_SETTLEMENT, MAX_SETTLEMENT_PERCENTAGE, MAX_SETTLEMENT_AMOUNT, REPAYMENT_ACTION, PARENT_REF_ID, PARENT_VERSION_ID, PROCESS_ID, IS_MASTER_VERSION, VERSION_ID) VALUES (:ID, :REFERENCE_ID, :TYPE_CODE, :SUB_TYPE_CODE, :ACTIVE_CODE, :STAGE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :TEMPLATE, :IS_TEMPLATE, :ACCOUNT_NUMBER, :PRODUCT_CODE, :LOGIC_OF_SETTLEMENT, :MAX_SETTLEMENT_PERCENTAGE, :MAX_SETTLEMENT_AMOUNT, :REPAYMENT_ACTION, :PARENT_REF_ID, :PARENT_VERSION_ID, :PROCESS_ID, :IS_MASTER_VERSION, :VERSION_ID)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() FgAlsAccounts entity);

    @SqlQuery("UPDATE FG_ALS_ACCOUNTS SET ID = :ID, REFERENCE_ID = :REFERENCE_ID, TYPE_CODE = :TYPE_CODE, SUB_TYPE_CODE = :SUB_TYPE_CODE, ACTIVE_CODE = :ACTIVE_CODE, STAGE_CODE = :STAGE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, TEMPLATE = :TEMPLATE, IS_TEMPLATE = :IS_TEMPLATE, ACCOUNT_NUMBER = :ACCOUNT_NUMBER, PRODUCT_CODE = :PRODUCT_CODE, LOGIC_OF_SETTLEMENT = :LOGIC_OF_SETTLEMENT, MAX_SETTLEMENT_PERCENTAGE = :MAX_SETTLEMENT_PERCENTAGE, MAX_SETTLEMENT_AMOUNT = :MAX_SETTLEMENT_AMOUNT, REPAYMENT_ACTION = :REPAYMENT_ACTION, PARENT_REF_ID = :PARENT_REF_ID, PARENT_VERSION_ID = :PARENT_VERSION_ID, PROCESS_ID = :PROCESS_ID, IS_MASTER_VERSION = :IS_MASTER_VERSION, VERSION_ID = :VERSION_ID WHERE id = :id")
    public int update(@BindBean() FgAlsAccounts entity);

    @SqlQuery("DELETE FROM FG_ALS_ACCOUNTS WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
