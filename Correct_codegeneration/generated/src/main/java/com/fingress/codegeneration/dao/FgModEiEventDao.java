package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgModEiEvent;
import com.bsit.codegeneration.mapper.FgModEiEventMapper;
import java.util.*;

@RegisterRowMapper(FgModEiEventMapper.class)
public interface FgModEiEventDao {

    @SqlQuery("SELECT * FROM FG_MOD_EI_EVENT")
    public List<FgModEiEvent> findAll();

    @SqlQuery("SELECT * FROM FG_MOD_EI_EVENT WHERE id = :id")
    public Optional<FgModEiEvent> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO FG_MOD_EI_EVENT(ID, FILE_REF_ID, EVENT_CODE, EVENT_DIRECTION_CODE, EVENT_STATUS_CODE, SOURCE_ENDPOINT_CODE, TARGET_ENDPOINT_CODE, EVENT_REQ_FILE_NAME, EVENT_REQUEST_DATE, EVENT_REQUEST, EVENT_RES_FILE_NAME, EVENT_RESPONSE_DATE, EVENT_RESPONSE, EVENT_ERROR_FILE_NAME, EVENT_ERROR_DATE, EVENT_ERROR, ACTIVE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, MOD_EI_FILE_ID) VALUES (:ID, :FILE_REF_ID, :EVENT_CODE, :EVENT_DIRECTION_CODE, :EVENT_STATUS_CODE, :SOURCE_ENDPOINT_CODE, :TARGET_ENDPOINT_CODE, :EVENT_REQ_FILE_NAME, :EVENT_REQUEST_DATE, :EVENT_REQUEST, :EVENT_RES_FILE_NAME, :EVENT_RESPONSE_DATE, :EVENT_RESPONSE, :EVENT_ERROR_FILE_NAME, :EVENT_ERROR_DATE, :EVENT_ERROR, :ACTIVE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :MOD_EI_FILE_ID)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() FgModEiEvent entity);

    @SqlQuery("UPDATE FG_MOD_EI_EVENT SET ID = :ID, FILE_REF_ID = :FILE_REF_ID, EVENT_CODE = :EVENT_CODE, EVENT_DIRECTION_CODE = :EVENT_DIRECTION_CODE, EVENT_STATUS_CODE = :EVENT_STATUS_CODE, SOURCE_ENDPOINT_CODE = :SOURCE_ENDPOINT_CODE, TARGET_ENDPOINT_CODE = :TARGET_ENDPOINT_CODE, EVENT_REQ_FILE_NAME = :EVENT_REQ_FILE_NAME, EVENT_REQUEST_DATE = :EVENT_REQUEST_DATE, EVENT_REQUEST = :EVENT_REQUEST, EVENT_RES_FILE_NAME = :EVENT_RES_FILE_NAME, EVENT_RESPONSE_DATE = :EVENT_RESPONSE_DATE, EVENT_RESPONSE = :EVENT_RESPONSE, EVENT_ERROR_FILE_NAME = :EVENT_ERROR_FILE_NAME, EVENT_ERROR_DATE = :EVENT_ERROR_DATE, EVENT_ERROR = :EVENT_ERROR, ACTIVE_CODE = :ACTIVE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, MOD_EI_FILE_ID = :MOD_EI_FILE_ID WHERE id = :id")
    public int update(@BindBean() FgModEiEvent entity);

    @SqlQuery("DELETE FROM FG_MOD_EI_EVENT WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
