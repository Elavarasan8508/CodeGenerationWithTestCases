package com.bsit.codegeneration.dao;

import org.jdbi.v3.sqlobject.statement.SqlQuery;
import org.jdbi.v3.sqlobject.statement.SqlUpdate;
import org.jdbi.v3.sqlobject.statement.GetGeneratedKeys;
import org.jdbi.v3.sqlobject.customizer.Bind;
import org.jdbi.v3.sqlobject.customizer.BindBean;
import org.jdbi.v3.sqlobject.config.RegisterRowMapper;
import com.bsit.codegeneration.record.FgScheduleCode;
import com.bsit.codegeneration.mapper.FgScheduleCodeMapper;
import java.util.*;

@RegisterRowMapper(FgScheduleCodeMapper.class)
public interface FgScheduleCodeDao {

    @SqlQuery("SELECT * FROM FG_SCHEDULE_CODE")
    public List<FgScheduleCode> findAll();

    @SqlQuery("SELECT * FROM FG_SCHEDULE_CODE WHERE id = :id")
    public Optional<FgScheduleCode> findById(@Bind("id") Long id);

    @SqlUpdate("INSERT INTO FG_SCHEDULE_CODE(ID, END_DATE, NAME, PROCESSING_TYPE, START_DATE, TEMPLATE, TYPE_CODE, PARTY_ID, PARTY_VERSION_ID, DESCRIPTION, APPLICANT_PARTY_CODE, CONFIG_REF_ID, CATEGORY_CODE, ACTIVE_CODE, STATUS_CODE, CREATED_ON, CREATED_BY, LAST_UPDATED_ON, LAST_UPDATED_BY, LAST_AUTHORISED_ON, LAST_AUTHORISED_BY, SUB_TYPE_CODE, ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE, PROCESS_ID, REFERENCE_ID, VERSION_ID, IS_MASTER_VERSION, STAGE_CODE) VALUES (:ID, :END_DATE, :NAME, :PROCESSING_TYPE, :START_DATE, :TEMPLATE, :TYPE_CODE, :PARTY_ID, :PARTY_VERSION_ID, :DESCRIPTION, :APPLICANT_PARTY_CODE, :CONFIG_REF_ID, :CATEGORY_CODE, :ACTIVE_CODE, :STATUS_CODE, :CREATED_ON, :CREATED_BY, :LAST_UPDATED_ON, :LAST_UPDATED_BY, :LAST_AUTHORISED_ON, :LAST_AUTHORISED_BY, :SUB_TYPE_CODE, :ENTITY_TYPE_CODE, :ENTITY_SUB_TYPE_CODE, :PROCESS_ID, :REFERENCE_ID, :VERSION_ID, :IS_MASTER_VERSION, :STAGE_CODE)")
    @GetGeneratedKeys()
    public Long insert(@BindBean() FgScheduleCode entity);

    @SqlQuery("UPDATE FG_SCHEDULE_CODE SET ID = :ID, END_DATE = :END_DATE, NAME = :NAME, PROCESSING_TYPE = :PROCESSING_TYPE, START_DATE = :START_DATE, TEMPLATE = :TEMPLATE, TYPE_CODE = :TYPE_CODE, PARTY_ID = :PARTY_ID, PARTY_VERSION_ID = :PARTY_VERSION_ID, DESCRIPTION = :DESCRIPTION, APPLICANT_PARTY_CODE = :APPLICANT_PARTY_CODE, CONFIG_REF_ID = :CONFIG_REF_ID, CATEGORY_CODE = :CATEGORY_CODE, ACTIVE_CODE = :ACTIVE_CODE, STATUS_CODE = :STATUS_CODE, CREATED_ON = :CREATED_ON, CREATED_BY = :CREATED_BY, LAST_UPDATED_ON = :LAST_UPDATED_ON, LAST_UPDATED_BY = :LAST_UPDATED_BY, LAST_AUTHORISED_ON = :LAST_AUTHORISED_ON, LAST_AUTHORISED_BY = :LAST_AUTHORISED_BY, SUB_TYPE_CODE = :SUB_TYPE_CODE, ENTITY_TYPE_CODE = :ENTITY_TYPE_CODE, ENTITY_SUB_TYPE_CODE = :ENTITY_SUB_TYPE_CODE, PROCESS_ID = :PROCESS_ID, REFERENCE_ID = :REFERENCE_ID, VERSION_ID = :VERSION_ID, IS_MASTER_VERSION = :IS_MASTER_VERSION, STAGE_CODE = :STAGE_CODE WHERE id = :id")
    public int update(@BindBean() FgScheduleCode entity);

    @SqlQuery("DELETE FROM FG_SCHEDULE_CODE WHERE id = :id")
    public int delete(@Bind("id") Long id);
}
